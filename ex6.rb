# The line below means the variable called types_of_people has the value of 10.
types_of_people = 10
# The line below means the variable of x has the value of a string which will
# contain the value of the variable types_of_people interpolated in it when it
#is printed.
x = "There are #{types_of_people} types of people."
# The line below means the variable named binary has the strng value of "binary".
binary = "binary"
# The line below means the variable do_not has the value of the string "don't".
do_not = "don't"
# The line below means that the variable y has the value of a string which will
# have the variables binary and do_not interpolated inside.
y = "Those who know #{binary} and those who #{do_not}."

# The line below means that the value of x will be printed to the screen.
puts x
# The line below means that the value of y will be printed to the screen.
puts y

